    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
        
        if(userName.isEmpty()||password.isEmpty()||organization == null||employee == null||role==null)
        {
            JOptionPane.showMessageDialog(null, "You cannot leave any field empty", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
             Boolean isUnique = chkForUnique(userName);
        if (isUnique) {
            
        organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role,null);
        popData();
            nameJTextField.setText("");
            passwordJTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "An account already exists with this name.Please select another name", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        }
      

    }                                             

    private Boolean chkForUnique(String name) {
        boolean isUnique = true;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount u : o.getUserAccountDirectory().getUserAccountList()) {
                if (u.getUsername().equals(name)) {
                   isUnique=false;
                   break;
                }
            }

        }
    return isUnique;
    }//GEN-LAST:event_createUserJButtonActionPerformed

