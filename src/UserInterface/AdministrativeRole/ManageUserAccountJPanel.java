    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow=userJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else{
            int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove the user?","Warning", JOptionPane.YES_NO_OPTION);
            if(flag==0){

                        UserAccount ua = (UserAccount)userJTable.getValueAt(selectedRow, 0);
                        for (Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
                            org.getUserAccountDirectory().getUserAccountList().remove(ua);

                        }
                        popData();
                        JOptionPane.showMessageDialog(null, "User account Removed.", "Info", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
    }//GEN-LAST:event_removeBtnActionPerformed

