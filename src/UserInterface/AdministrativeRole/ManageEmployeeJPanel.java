    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        
        Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
        String name = nameJTextField.getText();
        
         
        if(checkIfAlreadExist(organization)){
            
            JOptionPane.showMessageDialog(null, "Manager has already been created", "Info", JOptionPane.INFORMATION_MESSAGE);
        
        }else
        
        { organization.getEmployeeDirectory().createEmployee(name);
        populateTable(organization);
        
        }
        
    }//GEN-LAST:event_addJButtonActionPerformed

     private boolean checkIfAlreadExist(Organization organization) {
        boolean isExist = false ;
        System.out.println("Organization :" +organization.getSupportedRole());
        if (enterprise.getEnterpriseType().getValue().equals("Supplier")) {
         for (Organization o : organizationDir.getOrganizationList()){
        if (organization.getSupportedRole().equals(o.getSupportedRole())) {
                    isExist = true ;
            }
           
        }
     }
         return isExist ; 
  }
     
     
