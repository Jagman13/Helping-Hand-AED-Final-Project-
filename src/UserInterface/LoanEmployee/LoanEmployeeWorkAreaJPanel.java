    private void assignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignButtonActionPerformed
        int selectedRow = loanjTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request from the table");
        } else {

            WorkRequest request = (WorkRequest) loanjTable.getValueAt(selectedRow, 4);
            if (request.getReceiver() != null) {
                JOptionPane.showMessageDialog(null, "This request has alread been assigned");
            } else if((request.getStatus().equalsIgnoreCase("Approved") && (request.getResolveDate()==null)) || request.getStatus().equalsIgnoreCase("Intermidate") || request.getStatus().equalsIgnoreCase("Verified") || request.getStatus().equalsIgnoreCase("Rejected") ||request.getStatus().equalsIgnoreCase("Pending with loan approval")) {
            
            JOptionPane.showMessageDialog(null, "Cannot assign !! This request is ethier alrady approved or in intermidate state","info",JOptionPane.INFORMATION_MESSAGE);
            }else {
                request.setReceiver(account);
                request.setStatus("Pending with" +account.getEmployee().getName());
              //  populateProcessTable();
                populateTable();
            }

        }
    }//GEN-LAST:event_assignButtonActionPerformed

    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed
         int selectedRow = loanjTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request from the table");
        } else {

            WorkRequest request = (WorkRequest) loanjTable.getValueAt(selectedRow, 4);
            
            
            
            if(request.getStatus().equalsIgnoreCase("Approved") || request.getStatus().equalsIgnoreCase("Intermidate") ) {
            
            JOptionPane.showMessageDialog(null, "This request is already in other state","info",JOptionPane.INFORMATION_MESSAGE);
            }else if (request.getReceiver() !=null ){
                    if(request.getReceiver().equals(account)){
                request.setSender(account);
                request.setStatus("Intermidate") ;
                request.setReceiver(null);
               // populateProcessTable();
                populateTable();
            
                    }}else {
            
            JOptionPane.showMessageDialog(null, "Kindly assign the request first!!","info",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_processButtonActionPerformed

