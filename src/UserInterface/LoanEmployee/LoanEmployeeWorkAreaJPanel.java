    private void populateTable() {
    
    DefaultTableModel model = (DefaultTableModel) loanjTable.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
             System.out.println("request" +request);
             
           BankRequest req=(BankRequest)request;
          
            Object[] row = new Object[6];
            row[0] = req.getRequestDate();
            row[1] = req.getBankName();
            row[2] = req.getFarmerName();
            row[3] = req.getStatus() == "Sent to the Bank" ? "Pending with Bank Loan Dept" : req.getStatus();
            row[4] = request;
            row[5] = req.getResolveDate();   
            model.addRow(row);
    
    }
    }
    

