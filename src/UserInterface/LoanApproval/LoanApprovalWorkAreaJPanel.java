    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) loanApprovalWorkJTable.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
            BankRequest req =(BankRequest)request;
            Object[] row = new Object[6];
            row[0] = req.getRequestDate();
            row[1] = req.getFarmerName();  
            row[2] = req.getStatus();
            row[3] = request;
            row[4] = request.getReceiver();
            row[5] = req.getResolveDate();
            model.addRow(row);
        }
    }

