    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow =loanApprovalWorkJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request from the table");
        } 
        else {
            WorkRequest request = (WorkRequest) loanApprovalWorkJTable.getValueAt(selectedRow, 3);
            if (request.getReceiver() != null) {
                JOptionPane.showMessageDialog(null, "This request is already assigned");
            }else if (request.getStatus().equalsIgnoreCase("Disapproved")) {
            JOptionPane.showMessageDialog(null,"The request has been already disapproved ", "warning", JOptionPane.WARNING_MESSAGE);
            }else if (request.getStatus().equalsIgnoreCase("Intermidate") ||request.getStatus().equalsIgnoreCase("Sent to the Bank") || request.getStatus().equalsIgnoreCase("Pending with Bank Loan Dept")||request.getStatus().equalsIgnoreCase("Verified") || request.getStatus().equalsIgnoreCase("Rejected")) {
            
             JOptionPane.showMessageDialog(null,"The request is assigned to some other Department", "warning", JOptionPane.WARNING_MESSAGE);
            }
            
            else { 
                request.setReceiver(account);
                populateTable();
               
        }}
    }//GEN-LAST:event_assignBtnActionPerformed

    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        
         DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
          Date date = new Date();
        int selectedRow = loanApprovalWorkJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a request from the table","Info", JOptionPane.INFORMATION_MESSAGE);
        }else {
         WorkRequest request = (WorkRequest) loanApprovalWorkJTable.getValueAt(selectedRow, 3);
         
         if(request.getStatus().equals("Approved")) {
           JOptionPane.showMessageDialog(null, "This request is already approved", "Info", JOptionPane.INFORMATION_MESSAGE);  
         }else if (request.getStatus().equalsIgnoreCase("Disapproved")) {
             
            JOptionPane.showMessageDialog(null,"The request has been already disapproved ", "warning", JOptionPane.WARNING_MESSAGE);
         }else if (request.getReceiver() != null && request.getReceiver() != account) {
        
        JOptionPane.showMessageDialog(null,"The request is not assigned to you !!", "warning", JOptionPane.WARNING_MESSAGE);
        }else if (request.getReceiver() == null){ 
         JOptionPane.showMessageDialog(null,"Assign the request first !!", "warning", JOptionPane.WARNING_MESSAGE);
        
        }else {
             
                request.setStatus("Approved");
                request.setResolveDate(dateFormat.format(date));
                
                populateTable();
                
        }}
    }//GEN-LAST:event_approveBtnActionPerformed

