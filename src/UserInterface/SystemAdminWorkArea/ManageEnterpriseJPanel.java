    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
       int selectedRow=enterpriseJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else{
            int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove this Enterprise?","Warning", JOptionPane.YES_NO_OPTION);
            if(flag==0){
                Enterprise ent=(Enterprise)enterpriseJTable.getValueAt(selectedRow, 0);
               for (Network nWork : system.getNetworkList()) {
                    nWork.getEnterpriseDirectory().removeEnterprise(ent);
        }   
                populateTable();
                JOptionPane.showMessageDialog(null, "Enterprise Removed.", "Info", JOptionPane.INFORMATION_MESSAGE);

            }
        }
    }//GEN-LAST:event_removeJButtonActionPerformed

