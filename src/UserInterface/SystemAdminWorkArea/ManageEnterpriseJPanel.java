    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        String name = nameJTextField.getText();
        Network network = (Network) networkJComboBox.getSelectedItem();
        String address = addressjTextArea.getText();
        LOGGER.log(Level.INFO, "Address of Enterprise :" +address);
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null || name.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You cannot leave any field empty", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else {
            
            Boolean isUnique = IsNameUnique(name,network,type);
        if (isUnique) {
             Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);
              LOGGER.log(Level.INFO, "Enterprise details:" +enterprise.getName());
             enterprise.setAddress(address);
             JOptionPane.showMessageDialog(null, "Entrprise created", "Info", JOptionPane.INFORMATION_MESSAGE);
             populateTable();
        }else {
                JOptionPane.showMessageDialog(null, "Entrprise already exists with this name.Please select another name", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } 

    }//GEN-LAST:event_submitJButtonActionPerformed
    

private boolean IsNameUnique(String name, Network network , Enterprise.EnterpriseType type) {
        boolean isUnique = true;
        for ( Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
           
                
                if (enterprise.getName().equals(name) && enterprise.getEnterpriseType().equals(type) ) {
                   isUnique=false;
                   break;
                }
    
        }
    return isUnique;
    }     
