    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        
       
         int selectedRow =backgroundCheckjTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request from the table");
        } 
        else{
            
            WorkRequest request = (WorkRequest) backgroundCheckjTable.getValueAt(selectedRow, 4);
            if (request.getReceiver() != null) {
                JOptionPane.showMessageDialog(null, "This request is already assigned");
            } else if((request.getStatus().equalsIgnoreCase("Approved") && (request.getResolveDate() !=null))||request.getStatus().equalsIgnoreCase("Verified") || request.getStatus().equalsIgnoreCase("Rejected") ||request.getStatus().equalsIgnoreCase("Pending with loan approval") ) {
            
            JOptionPane.showMessageDialog(null, "This request is already completed","info",JOptionPane.INFORMATION_MESSAGE);
            }else if(request.getStatus().equalsIgnoreCase("Sent to the Bank") || request.getStatus().equalsIgnoreCase("Pending with Bank Loan Dept")){
              JOptionPane.showMessageDialog(null, "This request is with the Loan Dept. Wait till it is prcoessed from that department","info",JOptionPane.INFORMATION_MESSAGE);
            }else {
            request.setReceiver(account);
            populateTable();
        } }
            

        
    }//GEN-LAST:event_assignBtnActionPerformed

    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:
         int selectedRow = backgroundCheckjTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a request from the table");
        }
        
        WorkRequest request = (WorkRequest)backgroundCheckjTable.getValueAt(selectedRow, 4);
       
        if(request.getReceiver() == account){
        request.setStatus("Processing with" +account.getEmployee().getName());
        
        BackgroundProcessJPanel background = new BackgroundProcessJPanel(container, request,account);
        container.add("BackgroundProcessJPanel", background);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
        }else {
        
        JOptionPane.showMessageDialog(null, "This request is not assigned to you !!!" ,"warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_processBtnActionPerformed

