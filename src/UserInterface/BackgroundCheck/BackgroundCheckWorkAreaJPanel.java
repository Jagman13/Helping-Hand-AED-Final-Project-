    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) backgroundCheckjTable.getModel();
        
        model.setRowCount(0);
        
         for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
            BankRequest req =(BankRequest)request;
            Object[] row = new Object[5];
            row[0] = req.getRequestDate();
            row[1]=  req.getFarmerName();
            row[2] = req.getReceiver()==null ? "Not assigned" : req.getReceiver()  ;
            row[3] = req.getStatus();
            row[4] = request; 
            model.addRow(row);
        }
    }

